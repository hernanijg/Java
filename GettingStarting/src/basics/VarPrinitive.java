package basics;

public class VarPrinitive {

    // Numbers (Always start in 0<L,f,d>)
    //-128 to 127
    byte num1; // 8 bits -> 1 byte
    //-32768 same possitive
    short num2;  // 16 bits -> 2 byte
    //-2147483648 to same possitive
    int num3; // 32 bits -> 4
    // -900000000000000 (A LOT)
    long num4;// 64 bits -> 8 byte
    // 32 bits -> 4
    float realFloat = 3.1316f;
    // 64 bits -> 8
    double realDouble = 4.70E3;

    // Boolean > 1 bit (Default bool is false)
    boolean a = true;
    boolean b = false;

    // String (Default char is u0000)
    // 16 bites -> 2  byte
    char _a = 'a';
    char _b = '\u0021';
    char _c = '\t';
    
}
